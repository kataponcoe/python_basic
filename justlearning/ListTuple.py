# Operator (oprator)

a = 2
b = 4

# Oprator +

hasiltambah = a + b
print(hasiltambah)

# Oprator -

hasilkurang = a - b
print(hasilkurang)

# Oprator *

hasilperkalian = a * b
print(hasilperkalian)

# Oprator **

hasilpangkat = a ** b
print(hasilpangkat)

# Oprator /

hasilpembagian = a / b
print(hasilpembagian)

# Oprator // (div)

hasildiv = a // b
print(hasildiv)

# Oprator % (Mod)

hasilmod = a % b
print(hasilmod)
